class Op3{
    public static void main(String args[]){
        int a =10;
        int b=5;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b); //prints out the quotient
        System.out.println(a%b); //prints out the remainder
    }
}