public class printascii_typecasting {
    public static void main(String args[]){
        char ch1 = 'a';
        char ch2 = 'b';
        int ascii1 = (int) ch1;
        int ascii2 = (int) ch2;

        System.out.println("ASCII value of " + ch1 + " is " + ascii1);
        System.out.println("ASCII value of " + ch2 + " is " + ascii2);
    }
}
